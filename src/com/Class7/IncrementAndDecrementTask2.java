package com.Class7;

public class IncrementAndDecrementTask2 {

	public static void main(String[] args) {
		
		//Post increment a++
		
		int a =299;
		int b =a++; //first assign-->increment 
		
		System.out.println(a); //300
		System.out.println(b); //299
		 
		
		//Post decrement x--
		
		int z=10;
		int c=z--;
		
		System.out.println(z); //9
		System.out.println(c); //10

	}

}
